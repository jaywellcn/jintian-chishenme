/**
 * 根据日期获取当前节气
 * @param {Date} date 
 * @returns {Object} 节气信息
 */
function getSolarTerm(date) {
  const dateStr = formatDate(date);
  const monthDay = dateStr.slice(5); // MM-DD
  
  // 遍历查找当前节气
  for (let i = 0; i < SOLAR_TERMS_2025.length; i++) {
    const term = SOLAR_TERMS_2025[i];
    if (term.end >= term.start) {
      // 普通情况：在同一年内
      if (monthDay >= term.start && monthDay <= term.end) {
        return {
          ...term,
          detail: SOLAR_TERM_DETAIL[term.name],
          emoji: SOLAR_TERM_DETAIL[term.name]?.emoji || '',
          tip: SOLAR_TERM_DETAIL[term.name]?.tip || ''
        };
      }
    } else {
      // 跨年情况（如冬至 end=01-04, start=12-21）
      if (monthDay >= term.start || monthDay <= term.end) {
        return {
          ...term,
          detail: SOLAR_TERM_DETAIL[term.name],
          emoji: SOLAR_TERM_DETAIL[term.name]?.emoji || '',
          tip: SOLAR_TERM_DETAIL[term.name]?.tip || ''
        };
      }
    }
  }
  
  // 兜底：返回最近节气
  return {
    ...SOLAR_TERMS_2025[0],
    detail: SOLAR_TERM_DETAIL[SOLAR_TERMS_2025[0].name],
    emoji: '🌱',
    tip: '顺时而食'
  };
}

/**
 * 获取城市对应的饮食区域
 * @param {string} city 城市名
 * @returns {string} 饮食区域
 */
function getDietRegion(city) {
  if (!city) return '华北';
  
  // 精确匹配
  if (REGION_MAP[city]) return REGION_MAP[city];
  
  // 模糊匹配（去掉"市"后缀）
  const cleanCity = city.replace(/市$/, '');
  if (REGION_MAP[cleanCity]) return REGION_MAP[cleanCity];
  
  // 前缀匹配
  for (const key of Object.keys(REGION_MAP)) {
    if (key.startsWith(cleanCity) || cleanCity.startsWith(key)) {
      return REGION_MAP[key];
    }
  }
  
  return '华北'; // 默认华北
}

/**
 * 格式化日期为 YYYY-MM-DD
 */
function formatDate(date) {
  const d = date || new Date();
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, '0');
  const day = String(d.getDate()).padStart(2, '0');
  return `${y}-${m}-${day}`;
}

/**
 * 判断是否为今天
 */
function isToday(dateStr) {
  return formatDate(new Date()) === dateStr;
}

